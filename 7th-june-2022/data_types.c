#include<stdio.h>

int main(){
    // datatypes
    int in = 10;
    float fl = 10.1;
    char ch = 'i';
    char string[100] = "i am a string";
    double dbl = 938403.934;
    
    // printing this datatype value
    printf("\ninteger val: %d", in);
    printf("\nfloat val: %f", fl);
    printf("\nchar val: %c", ch);
    printf("\nstring val: %s", string);
    printf("\ndouble val: %f", dbl);

    return 0;
}